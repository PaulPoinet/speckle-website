  If something does not work as intended, we encourage you to first visit the [forums](https://discourse.speckle.works) to see if there has been a previous post regarding it. If not, feel free to create a new post.

  Once you are sure that you have discovered a bug, head over to [GitHub](https://github.com/speckleworks) and create a new issue in the appropriate repository. If you are unsure of where to place the bug report, report the bug in the forums and someone will help point you in the right direction. Note that only bugs related to the repositories included within the GitHub organization should be reported in this way. Bugs related to third-party applications and add-ons built on top of or for Speckle should be reported to its distributor.
